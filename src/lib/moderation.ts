import 'server-only'

/**
 * Простая серверная модерация пользовательского текста (названия/описания
 * услуг, био мастера, тексты отзывов). Блокирует мат и явно сексуальные/
 * эскорт-формулировки — чтобы никто «по рофлу» не выставлял непристойные
 * услуги. Списки легко расширять. Это v1: матчинг по словам, без сложного
 * обхода пробелами — при необходимости усилим.
 */

// Корни: блокируем, если СЛОВО начинается с корня (ловит производные,
// но не срабатывает в середине легитимных слов вроде «команда»/«мандарин»).
const ROOT_PREFIXES = [
  'хуй', 'хуя', 'хуе', 'хуи', 'пизд', 'ебан', 'ебат', 'ебал', 'ебуч', 'ебну',
  'бляд', 'блят', 'мудак', 'мудач', 'пидор', 'пидар', 'пидр', 'гандон', 'дроч',
  'залуп', 'выеб', 'наеб', 'заеб', 'уеб', 'въеб', 'отъеб', 'съеб', 'подъеб',
  // явно сексуальные / эскорт-услуги
  'порно', 'порн', 'интим', 'эскорт', 'проститу', 'шлюх', 'минет', 'вебкам', 'блядк',
]

// Точные слова (с базовыми словоформами) — там, где префикс дал бы ложные
// срабатывания (мандарин, сукно, команда и т.п.).
const EXACT = new Set([
  'манда', 'манды', 'мандой', 'манде', 'мандень',
  'сука', 'суки', 'суку', 'сукой', 'суке', 'сучка', 'сучки',
  'елда', 'елды',
  'секс', 'секса', 'сексом',
])

function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[^a-zа-я0-9]+/gi, ' ')
    .split(/\s+/)
    .filter(Boolean)
}

export type ModerationResult = { ok: true } | { ok: false; reason: string }

const REASON = 'Текст содержит недопустимые или непристойные слова. Отредактируйте формулировку.'

/** Проверяет один или несколько текстовых полей. */
export function checkContent(...parts: (string | null | undefined)[]): ModerationResult {
  for (const part of parts) {
    if (!part) continue
    for (const token of tokenize(part)) {
      if (EXACT.has(token)) return { ok: false, reason: REASON }
      for (const prefix of ROOT_PREFIXES) {
        if (token.startsWith(prefix)) return { ok: false, reason: REASON }
      }
    }
  }
  return { ok: true }
}
